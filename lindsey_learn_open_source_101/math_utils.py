# -*- coding: utf-8 -*-


def add_two(a: int, b: int) -> int:
    """
    Add two integers together and return the result.

    :param a: first integer
    :param b: second integer
    :return: the sum of a and b

    Example::

        >>> add_two(1, 2)
        3
        >>> add_two(10, 20)
        30
    """
    return a + b
